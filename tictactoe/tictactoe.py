def result(board, action):
    """
    Returns the board that results from making move (i, j) on the board.
    """
    i, j = action
    if board[i][j] != EMPTY:
        raise Exception("Can't execute movement. Cell is already filled")
    nextPlayer = player(board)
    board[i][j] = nextPlayer
    return board


